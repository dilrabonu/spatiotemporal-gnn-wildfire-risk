"""
Phase 5D — Counterfactual Intervention Analysis

THE UNIQUE CONTRIBUTION OF THIS PAPER
--------------------------------------
No prior wildfire burn probability paper applies counterfactual GNN
intervention analysis. This script answers the management question:

  "What happens to burn probability if we intervene on the landscape?"

Three scenarios, all with calibrated uncertainty bounds:
  1. Fuel reduction 30%    — reduce CFL by 30% across all test nodes
  2. Firebreak strip       — set CFL=0 in a horizontal landscape band
  3. Ignition suppression  — reduce Ignition_Prob by 50% across test nodes

The GNN propagates intervention effects through spatial topology —
adjacent cells see changed neighborhood features → their predictions
also change. This is impossible in XGBoost (no topology) or CNN (fixed kernel).

USAGE
-----
    conda activate wildfire-gnn
    cd spatiotemporal_wildfire_gnn

    # Run all 3 scenarios with GAT (default, recommended)
    python scripts/phase5d_intervention.py

    # Specify architecture
    python scripts/phase5d_intervention.py --arch GAT
    python scripts/phase5d_intervention.py --arch GCN

    # Run single scenario
    python scripts/phase5d_intervention.py --scenario fuel_reduction
    python scripts/phase5d_intervention.py --scenario firebreak
    python scripts/phase5d_intervention.py --scenario ignition_suppression

PRE-CONDITIONS
--------------
    checkpoints/archive/phase5a_gat_best.pt
    data/processed/graph_data_enriched.pt
    data/features/feature_names.json
    data/features/target_transformer.pkl
    reports/tables/phase5b_gat_temperature.csv    (calibration T)

OUTPUTS
-------
    reports/tables/phase5d_intervention_summary.csv
    reports/tables/phase5d_{scenario}_effects.csv
    reports/predictions/phase5d_{scenario}_effects.npz
    reports/figures/  (generated by phase5d_make_figures.py)
"""

